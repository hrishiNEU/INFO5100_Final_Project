   private void populateLogs() {
    // Get the table model
    DefaultTableModel model = (DefaultTableModel) tblLog.getModel();

    // Check if any text field is empty before adding the row
    if (txtCalamity.getText().isEmpty() || txtDamage.getText().isEmpty() || txtArea.getText().isEmpty()) {
        return;  // Skip adding if any text field is empty
    }

    // Add a new row for each log entry
    Object row[] = new Object[3];
    row[0] = txtCalamity.getText();  // Get calamity description
    row[1] = txtDamage.getText();    // Get damage description
    row[2] = txtArea.getText();      // Get area description

    // Add the row to the table model
    model.addRow(row);

    // Optionally, clear the text fields after adding the log
    txtCalamity.setText("");
    txtDamage.setText("");
    txtArea.setText("");
}


