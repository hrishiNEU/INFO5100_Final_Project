    private void btnSendReportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendReportActionPerformed

        DamageAnalyst analyst = DamageAnalyst.findDamageAnalyst(2);

        FieldResponseCoordinator coordinator = FieldResponseCoordinator.findCoordinator(4);

        String calamityType = txtCalamity.getText();
        String area =  txtArea.getText();
        double damageExtent;
        try {
            damageExtent = Double.parseDouble(txtDamage.getText());
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Please enter a valid number for Damage Extent",
                    "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        analyst.sendDamageReport(coordinator, calamityType, damageExtent, area);

        JOptionPane.showMessageDialog(this, "Damage report sent to " + coordinator.getName() + "(Field Response Coordinator",
                "Report Sent", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnSendReportActionPerformed

