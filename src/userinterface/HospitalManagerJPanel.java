    private void populatePatientTable() {
        DefaultTableModel model = (DefaultTableModel) tblPatient.getModel();
        model.setRowCount(0); // Clear the table

        for (HospitalManager manager : HospitalManager.getAllManagers()) {
            for (Citizen patient : manager.getAllPatients()) {
                model.addRow(new Object[]{patient.getName(), patient.getCitizenId(), patient.getAddress(), patient.getNationality()});
            }
        }
    }

    private void populateHospitalCombo() {
    cmbHospital.removeAllItems(); // Clear existing items

    String[] hospitalNames = {
        "City General Hospital",
        "Sunrise Medical Center",
        "Harmony Health Clinic",
        "Green Valley Hospital",
        "Oceanview Medical Facility"
    };

    for (String hospitalName : hospitalNames) {
        cmbHospital.addItem(hospitalName);
    }
}


